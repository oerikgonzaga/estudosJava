package estudosjava.operadores;

public class Negacao {

    public static void main(String[] args) {

        // Operador de Negação (  !  )
        // Inverte o resultado de um boolean: se TRUE, exibe FALSE  / se FALSE, exibe TRUE

        boolean verdadeiro = false;
        System.out.println(" Resultado Invertido: " + !verdadeiro);
        System.out.println(" Resultado Correto: " + verdadeiro);
    }
}
