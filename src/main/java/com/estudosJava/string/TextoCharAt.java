package main.java.com.estudosJava.string;

public class TextoCharAt {

    public static void main(String[] args) {

        // Obter uma letra de um texto a partir de um index.

        char letra = "Erik Gonzaga".charAt(0);
        System.out.println(letra);

        // resultado será igual a "E"
        // caso a posição do index seja a 4 ela retornará o espaço vazio.
    }
}
